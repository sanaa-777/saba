function seedPostgres(pgdb) {
  seedData(pgdb);

  const sequenceTables = [
    'categories', 'tags', 'news', 'media', 'breaking_news', 'slider',
    'advertisements', 'admin_users', 'comments', 'polls', 'poll_options',
    'poll_votes', 'newsletter_subscribers', 'newsletter_campaigns', 'images',
    'news_sources', 'fetch_logs', 'deleted_articles', 'audit_log'
  ];

  for (const table of sequenceTables) {
    try {
      pgdb.exec(`SELECT setval(pg_get_serial_sequence('${table}', 'id'), COALESCE((SELECT MAX(id) FROM ${table}), 1), true)`);
    } catch(e) {}
  }

  // Auto-add working RSS sources if none exist
  try {
    const sourceCount = pgdb.prepare('SELECT COUNT(*) as cnt FROM news_sources').get();
    if (!sourceCount || sourceCount.cnt === 0) {
      console.log('No news sources found, adding default sources...');
      const defaultSources = [
        ['BBC Arabic', 'https://feeds.bbci.co.uk/arabic/rss.xml', 'rss', 2, 900, 1],
        ['Al Jazeera', 'https://www.aljazeera.com/xml/rss/all.xml', 'rss', 2, 900, 1],
        ['France 24 Arabic', 'https://www.francetvinfo.fr/titres.rss', 'rss', 2, 1200, 1],
        ['Reuters Arabic', 'https://www.reutersagency.com/feed/', 'rss', 2, 1200, 1],
        ['Sky News Arabia', 'https://www.skynewsarabia.com/web/rss.xml', 'rss', 2, 1200, 1]
      ];
      const insertSource = pgdb.prepare('INSERT INTO news_sources (name, url, source_type, category_id, fetch_interval, auto_publish, next_fetch_at, last_fetch_status) VALUES (?, ?, ?, ?, ?, ?, CURRENT_TIMESTAMP, ?)');
      for (const [name, url, type, catId, interval, autoPub] of defaultSources) {
        try {
          insertSource.run(name, url, type, catId, interval, autoPub, 'pending');
        } catch(e) { console.log('Source add error:', name, e.message); }
      }
    }
  } catch(e) { console.log('Source check error:', e.message); }
}

function initDatabase() {
  if (initialized) return;

  const pgdb = getDb();
  createSchema(pgdb);

  const count = pgdb.prepare('SELECT COUNT(*) as cnt FROM categories').get();
  if (!count || !count.cnt) {
    seedPostgres(pgdb);
  }

  // Run migrations
  try {
    const sourceCount = pgdb.prepare('SELECT COUNT(*) as cnt FROM news_sources WHERE is_active = 1').get();
    if (sourceCount && sourceCount.cnt > 0) {
      // Check if sources have broken URLs
      const brokenSources = pgdb.prepare("SELECT id, name, url FROM news_sources WHERE url LIKE '%saba.ye%' OR url LIKE '%almassirah.net%' OR url LIKE '%alarabiya.net%' OR url LIKE '%maribpress.net%' OR url LIKE '%newyemen.net%'").all();
      if (brokenSources.length > 0) {
        console.log(`Found ${brokenSources.length} broken sources, fixing...`);
        // Update broken sources to working URLs
        const fixes = [
          { url: '%saba.ye%', name: 'BBC Arabic RSS', newUrl: 'https://feeds.bbci.co.uk/arabic/rss.xml' },
          { url: '%almassirah.net%', name: 'Al Jazeera RSS', newUrl: 'https://www.aljazeera.com/xml/rss/all.xml' },
          { url: '%alarabiya.net%', name: 'France 24 RSS', newUrl: 'https://www.francetvinfo.fr/titres.rss' },
          { url: '%maribpress.net%', name: 'Disabled - maribpress', newUrl: null },
          { url: '%newyemen.net%', name: 'Disabled - newyemen', newUrl: null }
        ];
        for (const fix of fixes) {
          if (fix.newUrl) {
            pgdb.prepare("UPDATE news_sources SET name = ?, url = ?, last_fetch_status = 'pending', last_error = NULL WHERE url LIKE ?").run(fix.name, fix.newUrl, fix.url);
          } else {
            pgdb.prepare("UPDATE news_sources SET is_active = 0, last_fetch_status = 'disabled' WHERE url LIKE ?").run(fix.url);
          }
        }
        console.log('Fixed broken sources');
      }
    }
  } catch(e) { console.log('Migration error:', e.message); }

  initialized = true;
}

