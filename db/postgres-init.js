let db = null;
let initialized = false;
let worker = null;
let queryCounter = 0;

function ensureWorker() {
  if (!worker) {
    worker = new Worker(path.join(__dirname, 'pg-worker.js'), {
      env: process.env
    });
  }
  return worker;
}

function querySync(sql, params = []) {
  ensureWorker();
  const signalBuffer = new SharedArrayBuffer(4);
  const signal = new Int32Array(signalBuffer);
  const payloadPath = path.join(os.tmpdir(), `pgsync-${process.pid}-${Date.now()}-${++queryCounter}-${crypto.randomBytes(4).toString('hex')}.json`);

  worker.postMessage({ sql, params, signalBuffer, payloadPath });

  const waitResult = Atomics.wait(signal, 0, 0, 300000);
  if (waitResult === 'timed-out') {
    throw new Error('PostgreSQL query timed out');
  }

  const raw = fs.readFileSync(payloadPath, 'utf8');
  fs.unlinkSync(payloadPath);
  const parsed = JSON.parse(raw);

  if (!parsed.ok) {
    const err = new Error(parsed.error?.message || 'PostgreSQL query failed');
    if (parsed.error) Object.assign(err, parsed.error);
    throw err;
  }

  return parsed;
}

function replacePlaceholders(sql) {
  let index = 0;
  return sql.replace(/\?/g, () => `$${++index}`);
}

function normalizeSql(sql) {
  let normalized = String(sql || '').trim();

  normalized = normalized.replace(/datetime\('now',\s*'-1 hour'\)/gi, "NOW() - INTERVAL '1 hour'");
  normalized = normalized.replace(/datetime\("now",\s*'-1 hour'\)/gi, "NOW() - INTERVAL '1 hour'");

  if (/^INSERT\s+OR\s+IGNORE\s+INTO\s+/i.test(normalized)) {
    normalized = normalized.replace(/^INSERT\s+OR\s+IGNORE\s+INTO\s+/i, 'INSERT INTO ');
    normalized += ' ON CONFLICT DO NOTHING';
  } else if (/^INSERT\s+OR\s+REPLACE\s+INTO\s+/i.test(normalized)) {
    const match = normalized.match(/^INSERT\s+OR\s+REPLACE\s+INTO\s+([^(\s]+)\s*\(([^)]+)\)\s*VALUES\s*\(([^)]+)\)$/i);
    if (match) {
      const table = match[1];
      const columns = match[2].split(',').map((c) => c.trim());
      const values = match[3];
      const conflictTarget = columns[0];
      const updates = columns.slice(1).map((c) => `${c} = EXCLUDED.${c}`).join(', ');
      normalized = `INSERT INTO ${table} (${columns.join(', ')}) VALUES (${values}) ON CONFLICT (${conflictTarget}) DO UPDATE SET ${updates}`;
    } else {
      normalized = normalized.replace(/^INSERT\s+OR\s+REPLACE\s+INTO\s+/i, 'INSERT INTO ');
    }
  }

  return replacePlaceholders(normalized);
}

class PgStatement {
  constructor(sql) {
    this.sql = sql;
  }

  get(...params) {
    const result = querySync(normalizeSql(this.sql), params);
    return result.rows[0];
  }

  all(...params) {
    const result = querySync(normalizeSql(this.sql), params);
    return result.rows;
  }

  run(...params) {
    let sql = normalizeSql(this.sql);
    if (/^INSERT\s+INTO\s+/i.test(sql) && !/\bRETURNING\b/i.test(sql)) {
      sql += ' RETURNING *';
    }
    const result = querySync(sql, params);
    const firstRow = result.rows && result.rows[0] ? result.rows[0] : null;
    return {
      changes: result.rowCount || 0,
      lastInsertRowid: firstRow && Object.prototype.hasOwnProperty.call(firstRow, 'id') ? firstRow.id : null
    };
  }
}

class PgCompatDatabase {
  prepare(sql) {
    return new PgStatement(sql);
  }

  exec(sql) {
    const statements = String(sql)
      .split(';')
      .map((s) => s.trim())
      .filter(Boolean);

    for (const statement of statements) {
      querySync(statement, []);
    }
  }

  pragma() {
    return null;
  }
}

function getDb() {
  if (!db) {
    db = new PgCompatDatabase();
  }
  return db;
}

